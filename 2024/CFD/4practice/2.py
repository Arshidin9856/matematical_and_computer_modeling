import numpy as np
dt=0.1
dx=0.5
n=10
N=10
l=np.zeros((N+1,n+1))
for i in range(N):
    l[i][0]=1
    for j in range(1,n):
        l[i+1][j]=(dt/(dx**2))*(l[i][j+1]-2*l[i][j]+l[i][j-1])+l[i][j]
    l[i][n]=0
print(l)    


